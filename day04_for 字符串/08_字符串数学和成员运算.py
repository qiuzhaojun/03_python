"""
    通用操作
        数学运算
        成员运算
"""
# 1. + 容器拼接
message = "悟空" + "八戒"
print(message)  # 悟空八戒

# 2. * 重复生成
# 只能与整数运算
name = "悟空" * 2
print(name)  # 悟空悟空

# 3. < <= > >= == !=
# 依次比较两个容器中元素,一但不同则返回比较结果。
print("悟空" > "唐僧")  # 比较的是unicode编码值

# 4. 成员运算
# false
# print("水洞" in "我是花果山水帘洞齐天大圣孙悟空")
# print("洞帘水" in "我是花果山水帘洞齐天大圣孙悟空")
print("水帘洞" not in "我是花果山水帘洞齐天大圣孙悟空")

# true
print("水帘洞" in "我是花果山水帘洞齐天大圣孙悟空")
