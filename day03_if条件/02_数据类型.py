"""
    数据类型
        字符串str  整形int  浮点型float
          "内容"     10     10.3
"""
# 整形的字面值(各种写法)
# 十进制写法DEC：每位用十种状态计数，逢十进一，写法是0~9
data01 = 20
# 二进制写法BIN：每位用二种状态计数，逢二进一，写法是0 1
data02 = 0b10
# 八进制写法OCT：每位用八种状态计数，逢八进一，写法是0~7
data03 = 0o10
# 十六进制写法HEX：每位用十六种状态计数，逢十六进一，写法是0~9  a~f(15)
data04 = 0x20

# 浮点型的字面值(各种写法)
# 小数写法
data05 = 1.5
# 科学计数法写法(当小数超过4个零时,使用科学计数法代码更清晰)
# data06 = 0.00001
data06 = 1e-5
print(data06)# 1e-05


