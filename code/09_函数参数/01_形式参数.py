"""
    函数参数
        形式参数
            默认形参：可选
                    def 函数名(参数名1=默认值,参数名2=默认值)
            位置形参：必填
                    def 函数名(参数名1,参数名2)
"""


# 默认形参:def 函数名(参数名1=默认值,参数名2=默认值)
# 作用：实参可选
# 注意：必须从右向左依次存在
def func01(p1=1, p2=2, p3=3):
    print(p1)
    print(p2)
    print(p3)


func01(1, 2, 3)
func01(p3=30)


# 位置形参：def 函数名(参数名1,参数名2)
# 实参必填
def func02(p1, p2, p3):
    print(p1)
    print(p2)
    print(p3)


func02(1, 2, 3)
func02(1, 2, p3=3)
